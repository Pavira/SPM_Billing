from fastapi import APIRouter, HTTPException
from pydantic import BaseModel
from datetime import datetime, timezone
from app.core.firebase import get_firestore

router = APIRouter()


# ---------------------------
# Schemas
# ---------------------------
class CustomerCreate(BaseModel):
    name: str
    email: str
    phone: str | None = None
    address: str | None = None
    gstin: str | None = None
    panNumber: str | None = None


class CustomerUpdate(BaseModel):
    name: str | None = None
    email: str | None = None
    phone: str | None = None
    address: str | None = None
    gstin: str | None = None
    panNumber: str | None = None


# ---------------------------
# Create Customer
# ---------------------------
@router.post("/")
def create_customer(payload: CustomerCreate):
    try:
        db = get_firestore()
        doc_ref = db.collection("customers").document()

        now = datetime.now(timezone.utc)

        doc_ref.set(
            {
                "id": doc_ref.id,
                "name": payload.name,
                "email": payload.email,
                "phone": payload.phone,
                "address": payload.address,
                "gstin": payload.gstin,
                "panNumber": payload.panNumber,
                "is_active": True,
                "created_at": now,
                "updated_at": None,
            }
        )

        return {
            "success": True,
            "customer_id": doc_ref.id,
        }

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


# ---------------------------
# Update Customer
# ---------------------------
@router.put("/{customer_id}")
def update_customer(customer_id: str, payload: CustomerUpdate):
    db = get_firestore()
    print(customer_id)
    doc_ref = db.collection("customers").document(customer_id)
    doc = doc_ref.get()

    if not doc.exists:
        raise HTTPException(status_code=404, detail="Customer not found")

    update_data = {k: v for k, v in payload.model_dump().items() if v is not None}

    update_data["updated_at"] = datetime.now(timezone.utc)

    doc_ref.update(update_data)

    return {"success": True}


# ---------------------------
# Delete Customer (Soft Delete)
# ---------------------------
@router.delete("/{customer_id}")
def delete_customer(customer_id: str):
    db = get_firestore()
    doc_ref = db.collection("customers").document(customer_id)
    doc = doc_ref.get()

    if not doc.exists:
        raise HTTPException(status_code=404, detail="Customer not found")

    doc_ref.update(
        {
            "is_active": False,
            "updated_at": datetime.now(timezone.utc),
        }
    )

    return {"success": True}


# ---------------------------
# Get All Customers (Active Only) and sort by date descending
# ---------------------------
@router.get("/")
def get_all_customers():
    db = get_firestore()
    customers_ref = db.collection("customers")
    query = customers_ref.where("is_active", "==", True).order_by(
        "created_at", direction="DESCENDING"
    )
    docs = query.stream()

    customers = []
    for doc in docs:
        customer_data = doc.to_dict()
        customer_data["customer_id"] = doc.id
        customers.append(customer_data)

    return {"customers": customers}


# ---------------------------
# Get Customer by ID
# ---------------------------
@router.get("/{customer_id}")
def get_customer_by_id(customer_id: str):
    db = get_firestore()
    doc_ref = db.collection("customers").document(customer_id)
    doc = doc_ref.get()

    if not doc.exists:
        raise HTTPException(status_code=404, detail="Customer not found")

    customer_data = doc.to_dict()
    customer_data["id"] = doc.id

    return {"customer": customer_data}
